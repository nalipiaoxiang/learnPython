# 数值
# 在Python数值分三类: 整数,浮点数(小数),复数
# 在Python中所有整数都是int类型
a = 10
b = 20
# 在Python中整数的大小没有限制,可以是无限循环大的数
c = 1 ** 100
print(a)
print(b)
print(c)
# 如果数字过长,可以使用下划线作为分隔符
d = 123_456_789
print(d)
# 其他进制的数
# e = 0123 10进制数不能以0开头
# 其他进制的整数,只要数字打印时都是以十进制显示
# 二进制
f = 0b10
# 八进制
g = 0o10
# 十六进制
h = 0x10
print(f)
print(g)
print(h)
print(1-5)

# 浮点数(小数),在Python中所有的小数都是float类型
i = 1.23
print(i)
#对浮点数进行运算时,可能得到一个不精确的结果
print(2-2.23)
print(0.1 + 0.2)


