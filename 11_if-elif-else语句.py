# if-elif-else语句
# 语句:
#   if 条件表达式:
#       代码块
#   elif 条件表达式:
#       代码块
#   elif 条件表达式:
#       代码块
#   .......
#   else:
#       代码块
age = 17
if age > 200 :
    print('活够了')
elif age > 100 :
    print('活的挺久的')
elif age > 60 :
    print('退休了')
elif age >= 18 :
    print('成年了')
else:
    print('你还是小孩')